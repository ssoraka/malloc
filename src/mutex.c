#include "../includes/malloc.h"

pthread_mutex_t	g_mutex = PTHREAD_MUTEX_INITIALIZER;

void	lock(void)
{
	pthread_mutex_lock(&g_mutex);
}

void	*unlock(void *ptr)
{
	pthread_mutex_unlock(&g_mutex);
	return (ptr);
}